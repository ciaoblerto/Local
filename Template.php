        <h2>Day 1: Arrival and Exploring Old Town</h2>
        <h6>Morning:</h6>
        <p>Start your day at Old Town Square, the heart of Prague. Admire the iconic Astronomical Clock and its hourly show.
        Take a short walk to Church of Our Lady before Týn for breathtaking Gothic architecture.</p>
        <h6>Lunch</h6>
        <p>Enjoy traditional Czech dishes at U Medvídků, a cozy and historical restaurant.</p>
        <h6>Afternoon</h6>
        <p>Wander through the picturesque Josefov (Jewish Quarter) and visit the Old Jewish Cemetery and Spanish Synagogue.
        Head to the Charles Bridge for stunning views of the Vltava River and the Prague Castle.</p>
        <h6>Evening</h6>
        <p>Take in the city lights with a river cruise along the Vltava. Most cruises offer dinner, making it a memorable start to your Prague adventure.</p>
    </section>

    <section class="day">
        <h2>Day 2: Discovering Prague Castle and Beyond</h2>
        <h6>Morning:</h6>
        <p>Visit Prague Castle, starting with St. Vitus Cathedral and the stunning Golden Lane.
        Don’t miss the panoramic views of the city from the castle grounds.</p>
        <h6>Lunch</h6>
        <p>Head down to Malá Strana (Lesser Town) and enjoy lunch at Café Savoy, known for its elegant interior and delicious Czech-French cuisine.</p>
        <h6>Afternoon</h6>
        <p>Wander through the picturesque Josefov (Jewish Quarter) and visit the Old Jewish Cemetery and Spanish Synagogue.
        Head to the Charles Bridge for stunning views of the Vltava River and the Prague Castle.</p>
        <h6>Evening</h6>
        <p>Take in the city lights with a river cruise along the Vltava. Most cruises offer dinner, making it a memorable start to your Prague adventure.</p>
    </section>

    <section class="day">
        <h2>Day 3: Art, Culture, and Farewell</h2>
        <h6>Morning:</h6>
        <p>Begin your day at Vyšehrad, an ancient fort with serene gardens and stunning views of the city. Visit Basilica of St. Peter and St. Paul and the nearby cemetery.
        Stop by Dancing House, one of Prague’s modern architectural gems, for a quick photo op.</p>
        <h6>Lunch</h6>
        <p>Savor local cuisine at Kantýna, a meat-lover’s paradise with freshly prepared dishes.</p>
        <h6>Afternoon</h6>
        <p>Dive into Prague’s art scene with a visit to the National Gallery or Museum Kampa.
        If time permits, shop for souvenirs like Bohemian glass or marionettes at Havelské tržiště (Havel's Market).</p>
        <h6>Evening</h6>
        <p>Enjoy traditional Czech beer at a local pub like Lokál, known for its fresh Pilsner and hearty fare.</p>
